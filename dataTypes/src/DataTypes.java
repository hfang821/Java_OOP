public class DataTypes {
    public static void main(String[] args){
        //1 byte or 8 bit
        byte minByte = -128;
        byte maxByte = 127;
        //2 byte or 16 bits
        short minShort = -32_768;
        short maxShort = 32_767;
        //4 bytes or 32 bits
        int minInteger = -2_147_483_648;
        int maxInteger = 2_147_483_647;
        //64 bit or 8 bytes;
        //specify an L at the end as a long value
        long minLong = -99999999999L;
        long maxLong = 999999999999L;
    }
}
