public class JavaOperators {
    public static void main(String[] args){
        int a = 10, b = 20, c = 15, d = 25;

        System.out.println("a+b= " + (a+b));
        System.out.println("a*b= " + (a*b));
        System.out.println("a/b= " + (a/b));
        System.out.println("c%a= " + (c%a));

        a++;
        System.out.println("a + 1 = " + a);
    }
}
