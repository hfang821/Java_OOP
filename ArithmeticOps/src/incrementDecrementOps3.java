public class incrementDecrementOps3 {
    public static void main(String[] args) {
        int x =100;
        System.out.println("value of ++x: " + ++x);
        System.out.println("value of x--: " + x--);
        System.out.println("value of x: " + x);
        /*
        101
        101
        100
         */
    }
}
